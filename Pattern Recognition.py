def checkio(pattern, image):
    prow = len(pattern)
    pcol = len(pattern[0])
    row = len(image)
    col = len(image[0])

    for i in range(row-prow+1):
        for j in range(col-pcol+1):
            if all(pattern[k][l] == image[i+k][j+l] for k in range(prow) for l in range(pcol)):
                for k in range(prow):
                    for l in range(pcol):
                        image[i+k][j+l] = pattern[k][l] + 2
    return image

